package GUI;

/**
 *
 * @author libby
 */
public class PointLine {
    private Point pointA;
    private Point pointB;

    public PointLine(Point a, Point b) {
        pointA = a;
        pointB = b;
    }

    public Point getPointA() {
        return pointA;
    }

    public Point getPointB() {
        return pointB;
    }
}
